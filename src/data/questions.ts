export function getQuestionsBySubject(subject: string): Question[] {
  return questions.filter((q) => q.subject === subject.toLowerCase());
}

export function getQuestionsByChapter(subject: string, chapter?: string): Question[] {
  const subjectQuestions = getQuestionsBySubject(subject);
  if (!chapter) return subjectQuestions;
  return subjectQuestions.filter((q) => q.chapter === chapter);
}
